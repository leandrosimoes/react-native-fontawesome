function translateFromCamelCaseToHyphened(camelCaseIconName) {
  return camelCaseIconName.split(/(?=[A-Z])/).map((camelCasePart, index, allParts) => {
    if (index == allParts.length - 1) {
      return camelCasePart.toLowerCase();
    } else {
      return camelCasePart.toLowerCase() + '-';
    }
  }).join('');
}

